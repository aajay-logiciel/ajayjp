/// [JPButtonSize] is used to set size of a button widget
enum JPButtonSize {
  /// Default size is [JPButtonSize.flat] is used to set flat size of a button widget
  flat,

  /// [JPButtonSize.large] is used to set large size of a button widget
  large,

  /// [JPButtonSize.medium] is used to set medium size of a button widget
  medium,

  /// [JPButtonSize.mediumWithIcon] is used to set medium mediumWithIcon of a button widget
  mediumWithIcon,

  /// [JPButtonSize.small] is used to set small size of a button widget
  small,

  /// [JPButtonSize.smallIcon] is used to set medium smallIcon of a button widget
  smallIcon
}
