/// [JPButtonColorType] is used to change background color, border color button widget.
enum JPButtonColorType {
  /// Default color type is [JPButtonColorType.primary], used for set primary color
  primary,

  /// [JPButtonColorType.tertiary], used for set tertiary color
  tertiary,

  /// [JPButtonColorType.lightGray], used for set light gray color
  lightGray,

  /// [JPButtonColorType.transparent], used for set transparent color
  transparent,
}
