/// [IconShape] is used to shape the IconButton widget.
enum JPIconShape {
  /// [JPIconShape.circle], used for rounded shape icon button
  circle,

  /// [IconShape.pills], used for pills shaped icon button with rounded corners
  pills,

  /// Default shape is [IconShape.standard], used for standard rectangle icon button with rounded corners
  standard,

  /// [IconShape.square], used for square icon button
  square,
}