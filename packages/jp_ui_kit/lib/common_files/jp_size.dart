/// [JPSize] is used to change the size of the widget.
class JPSize {
  /// [JPSize.SMALL] is used for small size widget
  static const double SMALL = 25;

  /// Default size if [JPSize.MEDIUM] is used for medium size widget
  static const double MEDIUM = 50;

  /// [JPSize.FULL] is used for full size widget
  static const double FULL = 100;
}

