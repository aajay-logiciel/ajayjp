/// [JPFontWeight] is used to change the fontweight of the text.
enum JPFontWeight {
  /// Default fontweight is [JPFontWeight.regular] is used to set fontweight of a text widget
  regular,

  ///  [JPFontWeight.medium] is used to set fontweight of a text widget
  medium,

  ///  [JPFontWeight.bold] is used to set fontweight of a text widget
  bold,
}
